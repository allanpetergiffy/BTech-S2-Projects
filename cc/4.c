// Usage of variables and format specifiers
#include <stdio.h>
int main(){
// create variables 
int myNum = 15; // Integer(whole number)
float myFloatNum = 5.99; // Floating point number
char myLetter = 'D'; // character 
// Print variables
printf("%d\n",myNum);
printf("%f\n",myFloatNum);
printf("%c\n",myLetter);
return 0;
}
